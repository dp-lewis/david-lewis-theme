      <p>
        On Thursday 4th February we held our 17th SydCSS. It was going to be an
        easy event for us. Speakers were lined up, the venue was booked, we had
        people at the venue prepped ready to set up the space as we needed it.
        We even had a little extra sparkle lined up in the form of the CSS
        Working Group being in town, and one of their co-chairs lined up as a
        surprise speaker. All was going swimmingly, but then at 11:48am on the
        day of the event we received a call that there was a major problem at
        the venue and they might not be able to host us.
      </p>

      <img src="https://www.david-lewis.com/wp-content/uploads/2016/02/ciab-768x512.jpg" />

      <p>
        If you run a meetup, this is a disaster. SydCSS can easily attract over
        100 people and finding a new location with only 6 hours and 42 minutes
        before you open the doors is not an easy task. But if you go and look at
        the feedback we’ve had from that night you’ll see that it was one of our
        most popular events.
      </p>

